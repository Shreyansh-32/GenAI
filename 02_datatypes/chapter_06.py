# String

customer_name = "Shreyansh Thakur"
chai_name = "Ginger chai"

print(f"Order for {customer_name} : {chai_name} please!")

print(f"First word : {customer_name.split(" ")[0]}")
print(f"Last word : {customer_name.split(" ")[1]}")

print(f"First word : {chai_name[:6]}")
print(f"Last word : {chai_name[7:]}")
print(f"Reverse string : {customer_name[::-1]}")